$('#add_region').click(function () {
    var number = Math.floor(Math.random()*11);
    $('#regions_table tbody').append('<tr id="region_' + number+'">');
    var td = $('<td>').append('<strong>' + $("#regions").val() + '</strong>');
    var td2 = $('<td>').append('<button type="button", class="btn btn-danger btn-rounded btn-sm" ' +
    'onClick=\'delete_row(\"region_' +number+'\")\' >' +
    '<span class=\"fa fa-times\"></span></button>');
    $('#region_'+ number).append(td);
    $('#region_'+ number).append(td2);
    $("#regions").val();
});
